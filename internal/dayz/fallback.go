// GetFallbackSpawnData returns pre-extracted spawn data for known maps.
// Used when live server data cannot be retrieved.
func GetFallbackSpawnData(mapName string) (*MapSpawnData, bool) {
	switch mapName {
	case "sakhal", "dayzOffline.sakhal":
		// Return a copy with current timestamps
		data := *sakhalSpawns
		data.FetchedAt = time.Now()
		data.CachedUntil = time.Now().Add(24 * time.Hour) // Fallback data is valid longer
		return &data, true
	default:
		return nil, false
	}
}
